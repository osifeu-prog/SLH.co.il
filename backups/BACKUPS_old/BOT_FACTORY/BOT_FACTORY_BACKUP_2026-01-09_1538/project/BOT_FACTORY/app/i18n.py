def normalize_lang(code: str | None) -> str:
    """
    ????? ??? ??? (??? he-IL, ru-RU) ?????? ?????:
    en / he / ru / es / ar
    """
    if not code:
        return "en"

    code = code.lower()

    if code.startswith("he"):
        return "he"
    if code.startswith("iw"):  # ?? ?????? ????? ?? ?????
        return "he"
    if code.startswith("ru"):
        return "ru"
    if code.startswith("es"):
        return "es"
    if code.startswith("ar"):
        return "ar"

    return "en"


